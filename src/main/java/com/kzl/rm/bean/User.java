package com.kzl.rm.bean;

public class User {
    private Long userId;

    private String userAccount;

    private String userName;

    private Byte userIdentity;

    private String position;

    private String email;

    private String telephone;

    private Long personalImageId;

    private String birthday;

    private String password;
    
    private Long original;
    
    private Long reprint;
    
    private Long translate;
    
    private Long visit;
    
    private Long discuss;
    
    private String imageurl;

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    public String getUserAccount() {
        return userAccount;
    }

    public void setUserAccount(String userAccount) {
        this.userAccount = userAccount;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public Byte getUserIdentity() {
        return userIdentity;
    }

    public void setUserIdentity(Byte userIdentity) {
        this.userIdentity = userIdentity;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public Long getPersonalImageId() {
        return personalImageId;
    }

    public void setPersonalImageId(Long personalImageId) {
        this.personalImageId = personalImageId;
    }

    public String getBirthday() {
        return birthday;
    }

    public void setBirthday(String birthday) {
        this.birthday = birthday;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

	public Long getOriginal() {
		return original;
	}

	public void setOriginal(Long original) {
		this.original = original;
	}

	public Long getReprint() {
		return reprint;
	}

	public void setReprint(Long reprint) {
		this.reprint = reprint;
	}

	public Long getTranslate() {
		return translate;
	}

	public void setTranslate(Long translate) {
		this.translate = translate;
	}

	public Long getVisit() {
		return visit;
	}

	public void setVisit(Long visit) {
		this.visit = visit;
	}

	public Long getDiscuss() {
		return discuss;
	}

	public void setDiscuss(Long discuss) {
		this.discuss = discuss;
	}

	public String getImageurl() {
		return imageurl;
	}

	public void setImageurl(String imageurl) {
		this.imageurl = imageurl;
	}
    
    
}